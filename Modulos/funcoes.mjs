function mostraIdade(nome, idade) {
    return `A Idade de ${nome} é ${idade} anos.`;
}

function mostraCidade(nome, cidade) {
    return `A Cidade de ${nome} é ${cidade}.`;
}

function mostraHobby(nome, hobby) {
    return `O Hobby de ${nome} é ${hobby}.`;
}

export {
    mostraIdade,
    mostraCidade,
    mostraHobby
};